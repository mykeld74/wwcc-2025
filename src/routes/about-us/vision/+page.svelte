	<div class="contentCard wrapper1">
		<h2 class="contentCardTitle">Loving and Following Christ</h2>
		<div class="cardContentWrapper">
			<div class="cardContent">
				<p class="contentCardText">
					<span class="cardAccent">LOVING CHRIST:</span> Paul said in Second Corinthians that it was
					the love of Christ that controlled him. It is this same motivation that drives us. It is out
					of love that we follow.
				</p>
				<p class="contentCardText">
					<span class="cardAccent">FOLLOWING CHRIST:</span> We want to follow Christ naturally. In John
					15, Jesus tells us to abide in Him; this abiding is essential to our healthy growing relationship
					with Him and our ultimate happiness in life. Abiding brings us connection and strength, as
					well as nourishment to live the life to which we’re called. Following Christ has to be the
					foundation of everything we do as a church. To minister outside of this relationship is wasted
					activity. When we follow Christ out of a motivation of love, we live lives of worship to Him.
				</p>
			</div>
		</div>
	</div>

	<div class="contentCard wrapper2">
		<h2 class="contentCardTitle">Loving and Serving Each Other</h2>
		<div class="cardContentWrapper">
			<div class="cardContent">
				<p class="contentCardText">
					<span class="cardAccent">LOVING EACH OTHER:</span> The Apostle John records these words of
					Jesus in his gospel: “My command is this: Love one another as I have loved you. Greater love
					has no man than this, that he lay his life down for his friends.” That is the level of love
					that Jesus expects us to have for one another. It is when the world sees that kind of love
					that lives are drawn to Christ.
				</p>
				<p class="contentCardText">
					<span class="cardAccent">SERVING EACH OTHER:</span> Love is meeting needs. When we love, we
					serve. The book of Acts is full of examples of people giving their lives away for each other.
					It is only when we put others first that we can fulfill God’s dream for us and our church.
				</p>
			</div>
		</div>
	</div>
	<div class="contentCard wrapper3">
		<h2 class="contentCardTitle">The Need for Balance</h2>
		<div class="cardContentWrapper">
			<div class="cardContent">
				<p class="contentCardText">
					We need to be extremely conscious as a church and also as individuals to maintain balance
					between these three essential elements:
				</p>
				<ul>
					<li>FOLLOWING CHRIST (WORSHIP)</li>
					<li>SERVING EACH OTHER (COMMUNITY)</li>
					<li>REACHING THE LOST (OUTREACH)</li>
				</ul>
				<p class="contentCardText">
					If we are to be effective as individuals and as a church, this balance is crucial. If we
					emphasize one and not the others we will be out of balance and thus not in a position to
					make a maximum impact.
				</p>
			</div>
		</div>
	</div>
</div>
